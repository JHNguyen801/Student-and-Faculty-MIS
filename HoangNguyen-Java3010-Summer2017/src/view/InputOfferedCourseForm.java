// private helper method to store a list of classrooms that can be
    // assigned to this course
    private void storeClassroomListInForm() {
        // If there are any classrooms available, add them to the data model for 
        // the list on the form
        for (int i = 0; i < data.getListOfClassrooms().size(); i++) {
            classroomDataModel.addElement(data.getListOfClassrooms().get(i));
        }
        // Link the data model to the list on the form
        listOfClassrooms.setModel(classroomDataModel);
    }
