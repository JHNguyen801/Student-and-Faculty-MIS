private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
// TODO add your handling code here:
    // Create a new classroom object
            OfferedCourse newCourse = new OfferedCourse();

            // Retrieve the selected classroom from the list
        Classroom newclassroom = (Classroom) this.listOfClassrooms.getSelectedValue();
        // Set the classroom in the object
        newCourse.setClassroom(newclassroom);
        // Retrieve the course id
        String courseid = this.courseIDTextField.getText();
        // Set the course number in the object
        newCourse.setCourseId(courseid);

        // Retrieve the course ();
        // Set the course number in the objectname
        String coursename = this.courseNameField.getText();
        // Setthe course name in the object
        newCourse.setCourseName(coursename);

        // Store the object in the application data model list of courses
        this.data.getListOfClassBases().add(newCourse);
}//GEN-LAST:event_saveButtonActionPerformed

